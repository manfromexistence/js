  expect(source).toContain("local-machine-trust-env sets only the trust flags");
  expect(source).toContain("default DX package-json machine read path");
  expect(source).toContain("identity-bound packed v5 package-json resolver payloads");
  expect(source).toContain("borrows archived resolver strings from the process-cached shard mmap");
  expect(source).toContain("keeps the root exports/imports value machine-backed at load time");
  expect(source).toContain("materializes resolver maps only as needed");
  expect(source).not.toContain('"local-machine-fast"');
  expect(source).not.toContain('"local-machine-read"');
  expect(source).not.toContain('"local-machine-shadow"');
  expect(source).toContain("generationExcludedFromTiming: true");
  expect(source).toContain('from "./dx-machine-cache-runtime-benchmark-deltas.ts"');
  expect(source).toContain("deltas: buildDeltas(summaries)");
  expect(deltaSource).toContain("type BenchmarkDelta");
  expect(deltaSource).toContain("const minMeaningfulEffectPct = 3");
  expect(deltaSource).toContain('"inconclusive"');
  expect(deltaSource).toContain('findTargetSummary(summaries, "official-json")');
  expect(deltaSource).toContain('findTargetSummary(summaries, "local-json")');
  expect(deltaSource).toMatch(/buildBaselineDeltas\(\s*officialBaseline/);
  expect(deltaSource).toMatch(/buildBaselineDeltas\(\s*localJsonBaseline/);
  expect(deltaSource).toContain("localMachineTargetNames.has(summary.target)");
  expect(deltaSource).toContain('"local-machine-integrated"');
  expect(deltaSource).toContain('"local-machine-rooted"');
  expect(deltaSource).toContain('"local-machine-trust-env"');
  expect(deltaSource).not.toContain('"local-machine-packed-doc-fast"');
  expect(deltaSource).not.toContain('"local-machine-fast-trust-document"');
  expect(source).toContain("| comparison | candidate vs baseline | min effect % | rankable | faster |");
  expect(source).not.toContain("| comparison | candidate vs official | rankable | faster |");
